"""
მაგალითი 1: პირველი ფანჯარა
ეს არის ყველაზე მარტივი tkinter პროგრამა
"""

import tkinter as tk

# მთავარი ფანჯრის შექმნა
root = tk.Tk()

# ფანჯრის სათაურის დაყენება
root.title("ჩემი პირველი ფანჯარა")

# ფანჯრის ზომის დაყენება (სიგანე x სიმაღლე)
root.geometry("400x300")

# ფანჯრის გაშვება
# ეს ხაზი უნდა იყოს ყოველთვის ბოლოში
root.mainloop()

